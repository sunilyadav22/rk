                        <div class="content-field">
                            <h3>Tutor(s)</h3>
                            <div>
                                <p>Richard Worden, Professor in the Department of Earth Ocean and Ecological Sciences,
                                    University of Liverpool, UK.</p>
                            </div>
                        </div>
                        <div class="content-field">
                            <h3>Overview</h3>
                            <div>
                                <p>This course will provide participants with awareness of the geoscience needs for CCS
                                    projects; namely subsurface CO2 storage volumetrics, CO2 flow in the subsurface away
                                    from injector wells, the goal of safe and permanent storage of CO2 and cost-benefit
                                    issues linked to aquifer depth, well design, etc. The course will establish basics,
                                    such as how much CCS is needed to make a difference to global warming, and explore
                                    what types of CO2 injection are already happening, including information from
                                    CO2-enhanced oil recovery projects. The course will deal with CO2 as a fluid phase
                                    and how much CO2 can be stored per cubic meter in terms of porosity and over entire
                                    aquifers. It will deal with how quickly CO2 can be injected and the role of aquifer
                                    permeability. The course then moves on to the all-important geomechanical effects of
                                    CO2 injection and feedbacks between induced mineral dissolution and rock strength
                                    and other rock properties. The full range of possible interaction between CO2 and
                                    both aquifer and top-seal will be covered, as will the range of possible leakage
                                    mechanisms that need to be assessed. The course will conclude with consideration of
                                    monitoring strategies.</p>
                            </div>
                        </div>
                        <div class="content-field">
                            <h3>Duration and Logistics</h3>
                            <div>
                                <p>Five 3.5-hour interactive online sessions presented over five days (mornings in North
                                    America and afternoons in Europe). Digital course notes and exercise materials will
                                    be distributed to participants before the course. Some exercises may be completed by
                                    participants off-line.</p>
                            </div>
                        </div>
                        <div class="content-field">
                            <h3>Level and Audience</h3>
                            <div>
                                <p><strong>Intermediate</strong>. The course is largely aimed at geoscientists, but
                                    engineers will also find the course instructive. Intended for sub-surface
                                    scientists, with an emphasis on geoscience topics. Participants will probably have a
                                    working knowledge of petroleum geoscience. However, the subject matter of this
                                    course, the geoscience of carbon capture and storage, is covered from basic
                                    principles.</p>
                            </div>
                        </div>
                        <div class="content-field">
                            <h3>Objectives</h3>
                            <div>
                                <p>You will learn to:</p>
                                <ol>
                                    <li>Develop awareness of the role of geoscience in CCS and of CCS in CO2 emissions
                                        reductions.</li>
                                    <li>Appreciate what CO2 injection projects have occurred so far and how they differ
                                        from industrial CCS.</li>
                                    <li>Understand CO2 as a fluid in the subsurface and the fluid injection pressure and
                                        effective stress regimes that CO2 injection will involve.</li>
                                    <li>Build awareness of the volumetrics of CO2 storage from the micro (pore-scale) to
                                        the macro (aquifer volumes).</li>
                                    <li>Gain an appreciation of the question of CO2 flow away from injector wells
                                        controlled by permeability and aquifer architecture.</li>
                                    <li>Understand the range of effects that CO2 can have on the host aquifer, from
                                        geomechanical to geochemical.</li>
                                    <li>Assess the role of top-seal and fault-seal properties and how they will
                                        influence CO2 storage, from risk of fracking, or induced seismicity, to mineral
                                        dissolution.</li>
                                    <li>Understand the range of ways that CO2 could escape from the planned storage
                                        sites.</li>
                                    <li>Develop an awareness of the range of monitoring strategies that could be
                                        employed to ensure safe and long-term storage of CO2.</li>
                                </ol>
                            </div>
                        </div>
                        <div class="content-field">
                            <h3>Course Content</h3>
                            <div>
                                <p><strong>Session 1 – Background, context, EOR-CCS and produced CO2-CCS</strong></p>
                                <ul>
                                    <li>Why is CCS needed?</li>
                                    <li>How does CCS fit into the bigger picture of CO2 emissions-reductions?</li>
                                    <li>The energy transition</li>
                                    <li>Examples of EOR-CCS</li>
                                    <li>Examples of in-field CCS</li>
                                    <li>Industrial scale CCS</li>
                                    <li>How is CO2 separated from flue and other gases: the regenerative amine process?
                                    </li>
                                    <li>The range of possible fates of CO2 in the subsurface as a function of time</li>
                                    <li>CO2 injection rates, constant or variable (seasonal, supply and economy effects)
                                    </li>
                                    <li>Injected CO2 purity and effect of other fluids (due to cost and practicality of
                                        CO2 separation)</li>
                                </ul>
                                <p>Exercises</p>
                                <ul>
                                    <li>Estimation of mass of CO2 locked away by CCS thus far</li>
                                    <li>Assessment of how many CCS projects are needed at current rate of emission</li>
                                    <li>Appraisal of mass of localised vs dispersed CO2 generation</li>
                                </ul>
                                <p><strong>Session 2 – Overview of geoscience context, CO2 in the subsurface and
                                        volumetrics</strong></p>
                                <ul>
                                    <li>Overview of geoscience context: volume/mass, flow, avoidance of leakage and
                                        lowest cost and energy</li>
                                    <li>CO2 P-T phase diagram</li>
                                    <li>Physical properties of CO2: density, wettability, viscosity and miscibility
                                        (water, oil)</li>
                                    <li>CO2-H2O mixing and water acidity</li>
                                    <li>CO2 storage per cubic metre, reservoir core porosity</li>
                                    <li>Controls on reservoir porosity</li>
                                    <li>What are typical aquifer porosities in different basinal settings?</li>
                                    <li>Extent and gross volume of reservoir (ultimate CO2 storage capacity) and sand
                                        body size</li>
                                    <li>Aquifer CO2 storage compartments</li>
                                </ul>
                                <p>Exercises</p>
                                <ul>
                                    <li>CO2 phase behavior in different aquifer pressure regimes</li>
                                    <li>Estimation of water acidity due to injected CO2</li>
                                    <li>Comparing aquifer porosity of CCS sites</li>
                                    <li>Calculation of CO2 storage in sandstones with different porosities (per cubic
                                        meter)</li>
                                    <li>Estimation of total storage capacity of CO2 in different aquifers</li>
                                </ul>
                                <p><strong>Session 3 – Flow of CO2 and geomechanical and chemical interactions with
                                        aquifer rocks</strong></p>
                                <ul>
                                    <li>Aquifer permeability and its effect on CO2 injection and flow</li>
                                    <li>What are typical aquifer permeabilities in different basinal settings?</li>
                                    <li>Controls on aquifer permeability</li>
                                    <li>Will permeability limit injection volumes?</li>
                                    <li>Aquifer architecture and connectivity and larger scale CO2 flow</li>
                                    <li>Comparison of advanced sedimentology in CCS work vs resource extraction</li>
                                    <li>Static and conceptual reservoir models for oil and gas vs aquifer models for CCS
                                    </li>
                                    <li>Stratigraphic and fault-seal barriers and baffles</li>
                                    <li>Injection pressures and CO2 flow rates</li>
                                    <li>Stress states in aquifers ahead of CCS (maximum and minimum principal stress)
                                    </li>
                                    <li>Fluid pressure and rock pressure related to failure</li>
                                    <li>Dilational and compactional failure of bulk rock</li>
                                    <li>Formation damage – does rock failure matter and will fractures help injectivity?
                                    </li>
                                    <li>Possible role of pre-existing faults and risk of leakage</li>
                                    <li>New fractures (fracking) near the well-bore region and risk of leakage</li>
                                </ul>
                                <p>Exercises</p>
                                <ul>
                                    <li>Injection rates and flow rate calculations for varying aquifer permeability</li>
                                    <li>Comparing aquifer permeability of CCS sites</li>
                                    <li>Deriving stress states from well data</li>
                                    <li>Assessing failure risk during injection based on rock strength and stress data
                                    </li>
                                </ul>
                                <p><strong>Session 4 – Geochemical interactions and geomechanical and rock property
                                        feedbacks with aquifer rocks</strong></p>
                                <ul>
                                    <li>Chemistries of CO2-water-rock interaction</li>
                                    <li>Dissolution of minerals in aquifers</li>
                                    <li>Range of minerals that might dissolve, roles of mineralogy and lithofacies</li>
                                    <li>Possible effects of dissolution on rock properties: geomechanical, porosity and
                                        permeability effects</li>
                                    <li>Where will dissolution occur: near the injection well or throughout aquifer?
                                    </li>
                                    <li>Driving force and rate of precipitation of new minerals in aquifers</li>
                                    <li>The abiding problem of predicted dawsonite precipitation</li>
                                    <li>Rate of mineral growth vs rate of injection to mitigate climate change</li>
                                    <li>Halite precipitation due to dehydration of highly saline aquifers: transient or
                                        permanent</li>
                                    <li>Types of top-seal lithologies and overview on effectiveness to trap CO2</li>
                                    <li>Characterization methods for top-seals: a greater drive than for oil and gas
                                        exploitation</li>
                                    <li>Top-seals at hydrocarbon sites vs aquifers, proven vs potential</li>
                                    <li>Mudstone top seal sedimentology, lateral continuity, thickness, brittle vs
                                        ductile behaviours (lessons from shale gas)</li>
                                    <li>Top-seal mineral dissolution risk</li>
                                    <li>Top-seal geomechanical failure risk</li>
                                    <li>CO2 column heights and top-seal capillary entry pressures</li>
                                </ul>
                                <p>Exercises</p>
                                <ul>
                                    <li>Classification of aquifers in terms of risk of mineral dissolution</li>
                                    <li>Prediction of possible effects of dissolution on porosity and permeability in
                                        near well-bore region</li>
                                    <li>Prediction of possible effects of dissolution on rock strength in near well-bore
                                        region</li>
                                    <li>Qualitative assessment of top-seal susceptibility to alteration based on
                                        mineralogy</li>
                                </ul>
                                <p><strong>Session 5 – Leakage scenarios and monitoring the CCS site</strong></p>
                                <ul>
                                    <li>CO2 column heights and top-seal capillary entry pressures</li>
                                    <li>Injection well completions and risk of cement dissolution</li>
                                    <li>Reiteration of risk of fracking due to high pressure injection</li>
                                    <li>Pre-existing open faults (seldom a problem in oil and gas fields)</li>
                                    <li>Escape of CO2 via thief zones; need to understand sedimentary architecture</li>
                                    <li>Escape via old, abandoned wells: how good are plugs?</li>
                                    <li>Flow to updip edge of aquifer and escape of CO2</li>
                                    <li>Monitoring strategies available: cost vs benefit</li>
                                    <li>4-D seismic and gravity and CO2 monitoring</li>
                                    <li>Seafloor monitoring</li>
                                    <li>Microseismic monitoring</li>
                                    <li>Monitoring wells</li>
                                </ul>
                                <p>Exercises</p>
                                <ul>
                                    <li>Calculation of CO2 column height based on top-seal SCAL data</li>
                                    <li>CO2 pressure variations linked to injection rates</li>
                                    <li>Detecting CO2 escape using geochemical data</li>
                                    <li>Prediction of effects of CO2 escape via unplugged wells on storage efficacy.
                                    </li>
                                </ul>
